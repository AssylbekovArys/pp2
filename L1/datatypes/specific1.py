#str
x = str("Hello World")

print(x)

print(type(x)) 

#int
x = int(20)

print(x)

print(type(x)) 

#float
x = float(20.5)

print(x)

print(type(x)) 

#complex
x = complex(1j)

print(x)

print(type(x))

#list
x = list(["apple", "banana", "cherry"])

print(x)

print(type(x))

#tuple
x = tuple(("apple", "banana", "cherry"))

print(x)

print(type(x))

#range
x = range(6)

print(x)

print(type(x))

#dict
x = dict({"name" : "John", "age" : 36})

print(x)

print(type(x))

#set
x = set({"apple", "banana", "cherry"})

print(x)

print(type(x))

#frozenset
x = frozenset({"apple", "banana", "cherry"})

print(x)

print(type(x))

#bool
x = bool(5)

print(x)

print(type(x))

#bytes
x = bytes(5)

print(x)

print(type(x))

#bytearray
x = bytearray(5)

print(x)

print(type(x))

#memoryview
x = memoryview(bytes(5))

print(x)

print(type(x))

#Nonetype
x = None

print(x)

print(type(x))